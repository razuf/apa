    assert "-12300" == ApaNumber.to_string({-123, 2}, 3, 0)
    assert "-1230000" == ApaNumber.to_string({-12_345, 2}, 3, 0)

    assert "123" == ApaNumber.to_string({12_345, -2}, 0, 0)
    assert "123" == ApaNumber.to_string({12_345, -2}, 3, 0)
